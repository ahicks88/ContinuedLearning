-- WHERE are the Subqueries?:
-------------------------------------------------------------------------------------------------------------------------------------
SELECT column
FROM (SELECT column
      FROM table) AS subquery;
-------------------------------------------------------------------------------------------------------------------------------------
SELECT home_goal
FROM match
WHERE home_goal > (
        SELECT AVG(home_goal)
        FROM match);
-------------------------------------------------------------------------------------------------------------------------------------
SELECT AVG(home_goal) FROM match;
